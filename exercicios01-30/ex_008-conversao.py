medida = float(input("Uma distância em metros: "))
milimetros = medida * 1000
centimetros = medida * 100
dencimetros = medida * 10
decametros = medida / 10
hectometros = medida / 100
kilometros = medida / 1000

print(f"A medida {medida}m em milímetros é {milimetros}")
print(f"A medida {medida}m em centímetros é {centimetros}")
print(f"A medida {medida}m em dencímetros é {dencimetros}")
print(f"A medida {medida}m em decâmetros é {decametros}")
print(f"A medida {medida}m em hectômetros é {hectometros}")
print(f"A medida {medida}m em kilometros é {kilometros}")
